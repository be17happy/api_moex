# ============================================
# НАСТРОЙКИ ЗАГРУЗКИ ДАННЫХ
# ============================================

# Количество акций для загрузки
# Рекомендуемые значения:
# - 5-10: Быстро, для тестирования
# - 20-30: Среднее, для демонстрации
# - 50-100: Полное, для анализа
# - 200+: BIG DATA режим (не помещается в RAM)
MAX_SECURITIES = 250

# Период истории торгов (дни)
# Рекомендуемые значения:
# - 7: Неделя (быстро)
# - 30: Месяц (среднее)
# - 90: Квартал (долго)
# - 365: Год (очень долго, может быть много данных)
# - 730+: BIG DATA режим (2+ года данных)
HISTORY_DAYS = 730

# Площадка торгов
# TQBR - основной режим торгов акций
# TQTF - ETF фонды
BOARD = 'TQBR'

# Сортировка акций
# 'RANDOM': случайные акции
# 'TOP': топовые по ликвидности (если доступно)
